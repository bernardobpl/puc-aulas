// sum
let age = 27; // 27
age = 27+1;   // 28
age += 1       // 29
age++         // 30

// substraction
age = 30;
age = 30-1;  // 29
age -= 1;    // 28
age--        // 27

// multiplication
let water = 2;
water = 2*2;    // 4
water *= 2;     // 8

// power (potência)
water **= 2;    // 64

// division
let pizza = 64;
pizza = 64/2;   // 32
pizza /= 2;     // 16

// modulo (resto da divisão)
pizza = 16;
pizza = 16%2;   // 0
pizza %= 2;     // 0